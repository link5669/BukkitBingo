package io.github.link5669.AchievementBingo;

public class PlayerScore {
	private int score;
	private String name;
	public void setPlayerScore(int score) {
		this.score = score;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPlayerScore() {
		return this.score;
	}
	public String getName() {
		return this.name;
	}
}